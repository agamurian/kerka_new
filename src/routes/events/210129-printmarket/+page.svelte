**upd:** _Принтмаркет был продлен до чертверга:_

Принтмаркет начинается в пятницу и идет 6 дней:<br/>
**пятница, суббота, воскресенье** - 16:00 - 22:00<br/>
**вторик, среда** - 16:00 - 22:00<br/>
Четверг - на закрытии принтмаркета [выступит Диана Лапина](/events/diana-lapina-040221)

Вы сможете купить оригинальные работы современных художников: принты, открытки, мерч и прочие тиражные или недорогие работы (до 100 евро).

![афиша](./images/pm-290121-v.jpg)

Весь прошлый год мы сталкивались с постоянными запретами на проведение выставок, общественных мероприятий, и при этом с открытыми барами и ресторанами, торговыми центрами. В самые жесткие времена общепит мог функционировать только "на вынос". Художники тоже выкручивались как, могли, проводя свои выставки в местах для этого не предназначенных, втихую и подпольно.

Наше мероприятие нацелено на поддержку художников, легализацию и распространение искусства в массовой культуре в эти не легкие для всех времена.

Выставка-ярмарка пройдет на базе галереи Kerka и станет регулярным событием, в противовес закрытым аукционам и прочим не регламентируемым продажам.

